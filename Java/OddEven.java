//Write a Java Program to display whether a number is odd or even.

import java.util.*;

public class OddOrEven {
    public static void main(String[] args) {
        int num = 4;
        if (num % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }
    }
}
